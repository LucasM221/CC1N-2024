SELECT ID_POST, TEXTO, IMAGEM, DATA_HORA, NOME_USUARIO
FROM postagens as P
join usuarios on p.usuario = id_usuario;
#EX5 - Listar o nome dos usuários e a quantidade de postagens que cada um fez.
SELECT ID_USUARIO,NOME_USUARIO, count(*) as qtd_postagens
FROM POSTAGENS AS P
JOIN USUARIOS AS u ON ID_USUARIO = P.USUARIO
GROUP BY nome_usuario;
#EX6 - Mostrar os nomes dos usuários que fizeram comentários em 2024.
SELECT NOME_USUARIO, ID_COMENTARIO, DATAHORA
FROM USUARIOS AS U
JOIN COMENTARIOS AS C ON ID_USUARIO = C.ID_COMENTARIO;
#EX7 - Listar todos os grupos, ordenados pelo nome do grupo de A a Z.
SELECT ID_GRUPO,NOME_GRUPO
FROM GRUPOS
ORDER BY NOME_GRUPO ASC;
#EX8
SELECT NOME_GRUPO,DESCRICAO
FROM GRUPOS
