#EX9 
SELECT NOME_USUARIO,ID_COMENTARIO,C.TEXTO,DATAHORA,ID_POST
FROM USUARIOS AS U
JOIN POSTAGENS AS P ON ID_USUARIO = P.ID_POST
JOIN COMENTARIOS AS C ON P.ID_POST = C.ID_COMENTARIO;
#EX10
SELECT ID_POST,P.TEXTO AS POSTAGEMTEXTO,C.TEXTO AS COMENTARIO,ID_COMENTARIO, COUNT(ID_COMENTARIO) AS QTD_COMENTARIOS
FROM POSTAGENS AS P
JOIN COMENTARIOS AS C ON P.USUARIO = C.USUARIO
GROUP BY P.ID_POST;
#EX11
